    private void jLabelMapaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelMapaMousePressed
        Point punkt = new Point(evt.getPoint());
        //jLabelMiejsce.setText(Double.toString(punkt.getX()));
        //jLabelCena.setText(Double.toString(punkt.getY()));

       
        
        
        Point p_Moskwa = new Point(435,190);
        Point p_Berlin = new Point(239,234);
        Point p_Madryt = new Point(67,353);
        Point p_Paryz  = new Point(149,273);
        Point p_Londyn = new Point(135,228);
        Point p_Rzym   = new Point(227,367);
        
        String nazwa_miasta = "";
        wybrane = null;//new Miasto();
        
        if(f.distancebetween2Points(punkt, p_Moskwa)<= 15){
            nazwa_miasta = "Moskwa";
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Moskwa.png");
            jLabelMapa.setIcon(obrazek);
        }
        else if(f.distancebetween2Points(punkt, p_Berlin)<= 15){
            nazwa_miasta = "Berlin";
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Berlin.png");
            jLabelMapa.setIcon(obrazek);
        }
        else if(f.distancebetween2Points(punkt, p_Madryt)<= 15){
            nazwa_miasta = "Madryt";
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Madryt.png");
            jLabelMapa.setIcon(obrazek);
        }
        else if(f.distancebetween2Points(punkt, p_Paryz)<= 15){
            nazwa_miasta = "Paryz";
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Paryz.png");
            jLabelMapa.setIcon(obrazek);
        }
        else if(f.distancebetween2Points(punkt, p_Londyn)<= 15){
            nazwa_miasta = "Londyn";
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Londyn.png");
            jLabelMapa.setIcon(obrazek);
        }
        else if(f.distancebetween2Points(punkt, p_Rzym)<= 15){
            nazwa_miasta = "Rzym";  
            ImageIcon obrazek = new ImageIcon("src/obrazy/mapa - Rzym.png");
            jLabelMapa.setIcon(obrazek);
        }
        
        List<Miasto> lista_miast = new ArrayList<Miasto>();
        try {
            lista_miast = f.getAllMiasta();
            
            for(Miasto m : lista_miast){
                //JOptionPane.showMessageDialog(null, m);
                if(nazwa_miasta.equals(m.getNazwa()))
                    wybrane = m;
            }
            if (wybrane != null){
            jLabelMiejsce.setText(wybrane.getNazwa());
            jLabelCena.setText(Integer.toString(wybrane.getCena()));
            }
            
         
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_jLabelMapaMousePressed

